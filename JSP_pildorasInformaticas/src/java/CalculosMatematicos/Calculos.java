public class Calculos {
   
    private static int res = 0;

            public static int MetodoSuma(int num, int num2) {

                res = num + num2;

                return res;

            }

            public static int MetodoResta(int num, int num2) {

                res = num - num2;

                return res;

            }

            public static int MetodoMultiplica(int num, int num2) {

                res = num * num2;

                return res;

            }
}
